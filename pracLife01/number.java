package pracLife01;

public class number {

	public static void main(String[] args) {
		
		System.out.println(6 + 2);
		System.out.println(6 - 2);
		System.out.println(6 * 2);
		System.out.println(6 / 2);
		//   +-*/연산자 Operator
		
		System.out.println(Math.PI);
		System.out.println(Math.floor(Math.PI)); //수학적 뜻으로 '내림'이란 뜻
		System.out.println(Math.ceil(Math.PI)); //'올림'이란 뜻
		
	}

}
