import kr.or.kosa.Bird;

//설계자 

class Bi extends Bird{
	@Override
	public void moveFast() {
		//마음대로...
		super.moveFast();  //부모꺼 ㄱㅏ져오기
	}
}

class Ostrich extends Bird{
	//구체화, 특수화
	void run() {
		System.out.println("run...");
	}
	@Override
	public void moveFast() {
		run();
	}
	
}

public class Ex09_Inherit_Protected {
	public static void main(String[] args) {
		Bi b = new Bi();
		b.fly();
		b.moveFast();  //protected를 쓰는 이유는 상속관계에서 내가 만든 함수를 당신이 재정의 해서 썻으면 좋겠다는 것이기 떄문이다
		//moveFast() 상속관계에서 재정의를 하지 않으면 당신을 사용할 수 없어(강제)
		
		Ostrich o = new Ostrich();
		o.run();
		o.moveFast();
	}
}
